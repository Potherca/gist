
# ==============================================================================
## Usage:
## Displays all lines in main script that start with '##'
# ------------------------------------------------------------------------------
print_usage_short() {
    [ "$*" ] && echo "$(basename "$0"): $*"
    sed -n '/^##/,/^$/s/^## \{0,1\}//p' "$0"
} #2>/dev/null
# ==============================================================================

# ==============================================================================
#/ Usage:
#/ Displays all lines in main script that start with '#/'
# ------------------------------------------------------------------------------
print_usage_full() {
    grep '^#/' <"$0" | cut -c4-
}
# ==============================================================================

# ==============================================================================
# ------------------------------------------------------------------------------
print_error_message() {
    local sMessage="$1"
    local iExitcode="${2:-0}"

    echo "" >&2
    echo " !     ERROR: $sMessage" >&2
    echo "" >&2

    if [[ ${iExitcode} -ne 0 ]]; then
        exit "${iExitcode}"
    fi
}
# ==============================================================================

# ==============================================================================
# ------------------------------------------------------------------------------
print_status_message() {
    echo "-----> $*"
}
# ==============================================================================

# ==============================================================================
# ------------------------------------------------------------------------------
print_topic_message() {
    echo
    echo "=====> $*"
}
